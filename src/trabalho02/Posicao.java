public class Posicao {

    protected double x;
    protected double y;

    /**
     * Construtor da classe Posicao esta classe representa a posicao das
     * unidades e construcoes no mapa.
     *
     * @param x a posicao em x da unidade e/ou construcao
     * @param y a posicao em y da unidade e/ou construcao
     */
    public Posicao(double x, double y) {
        this.x = x;
        this.y = y;
    }

}
